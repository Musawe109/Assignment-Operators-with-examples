        // Assignment Operators

   let x:number = 5;
   let y:number = 10;

   x += y  //x = x=y
   y += x  //y = y+x
   console.log(x)    


   let a:number = 5;
   let b:number = 10;

   a -= b  //a = a-b
   b -= a  //b = b-a
   console.log(b)    

   let c:number = 2;
   let d:number = 4;
    
   c *= d   //c = c*d
   d *= c   //d = d*c
   console.log(d) 

