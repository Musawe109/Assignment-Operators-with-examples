"use strict";
// Assignment Operators
let x = 5;
let y = 10;
x += y; //x = x=y
y += x; //y = y+x
console.log(x);
let a = 5;
let b = 10;
a -= b; //a = a-b
b -= a; //b = b-a
console.log(b);
let c = 2;
let d = 4;
c *= d; //c = c*d
d *= c; //d = d*c
console.log(d);
//Another Example 
let num1 = 2;
let num2 = 4;
num1 += num2; //num1 = num1 + num2
num2 += num1; //num2 = num2 + num1
